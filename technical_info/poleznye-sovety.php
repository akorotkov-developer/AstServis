				<h2>Рекомендации по эксплуатации и обслуживанию двигателей внутреннего сгорания</h2>
				<p>
					 Надежность, характеристики и качество работы двигателя напрямую зависят от того, как он эксплуатируется и обслуживается. Только мотор, который грамотно эксплуатируется и подвергается регулярному техническому обслуживанию, будет обеспечивать номинальные характеристики и безотказно служить на протяжении многих лет. Это в равной степени относится к бензиновым и дизельным силовым агрегатам независимо от мощности, объема и назначения.
				</p>
				<p>
					 Есть несколько основных правил эксплуатации двигателей внутреннего сгорания, которых стоит придерживаться всем автомобилистам, мотоциклистам и владельцам иных транспортных средств:
				</p>
				<p>
					 • Использовать только качественное топливо той марки (сорта), которая рекомендована производителем;<br>
					 • То же относится ко всем остальным жидкостям — моторному маслу, охлаждающей жидкости и т.д.;<br>
					 • Не допускать длительной работы на повышенных оборотах (не выше 1800-2100&nbsp;об/мин в зависимости от типа двигателя);<br>
					 • Свести к минимуму запуски двигателя «с толкача»;<br>
					 • Не начинать движение сразу после запуска двигателя — мотору нужно дать прогреться как минимум 3-5&nbsp;минут;<br>
					 • Сразу после пуска двигателя не стоит резко повышать обороты двигателя, прогрев должен проходить на холостых оборотах. Особенно это относится к двигателям, оборудованным турбокомпрессором&nbsp;— резко поддав газу можно вывести турбину из строя вследствие недостаточной смазки;<br>
					 • Не выключать двигатель при движении с горки — за мнимой экономией топлива скрывается опасность возникновения серьезных неисправностей;<br>
					 • Не останавливать двигатель сразу после активной или длительной поездки, сначала мотор должен в течение нескольких минут остыть на холостых оборотах. Особенно это актуально для двигателей, оборудованных турбокомпрессором;<br>
					 • В современных автомобилях стоит избегать отключения аккумулятора, так как в этом случае очень высок риск сбоя бортового компьютера и нарушения настроек работы двигателя;<br>
					 • Не допускать попадания воды в бензин и в двигатель, так как это чревато многими поломками;<br>
					 • Проводить регулярное техническое обслуживание.
				</p>
				<p>
					 Особо нужно обратить внимание на такую вещь, как холодный запуск двигателя. Мы живем в стране с холодным климатом, и на протяжении многих месяцев вынуждены эксплуатировать автомобиль при отрицательных и сильно отрицательных температурах. И если температура воздуха падает ниже -15°C&nbsp;... -20°C, то приходится прибегать к специальным приемам холодного пуска двигателя. Причем для моторов различного типа процедура холодного запуска отличается.
				</p>
				<p>
					 Холодный запуск карбюраторных бензиновых двигателей производится следующим образом:
				</p>
				<ol>
					<li>Поставить автомобиль на стояночный тормоз, рычаг переключения передач должен находиться в нейтральном положении, все электроприборы должны быть выключены;</li>
					<li>Если температура воздуха ниже -25°C, то в отечественных автомобилях, особенно старых, можно несколько раз провернуть двигатель с помощью ручки завода;</li>
					<li>Произвести ручную подкачку топлива с помощью рычага на бензонасосе. Достаточно нажать на рычаг 7-8&nbsp;раз, если накачать бензина больше, то он зальет свечи и запустить двигатель будет еще сложнее;</li>
					<li>Полностью вытянуть ручку управления воздушной заслонкой карбюратора&nbsp;— заслонка, перекрывая первую камеру карбюратора, снижает поступление воздуха, чем обеспечивается обогащение горючей смеси;</li>
					<li>Выключить сцепление (до упора нажать педаль сцепления), педаль газа во время пуска не стоит трогать;</li>
					<li>Повернуть ключ зажигания до упора вправо, произвести пуск. Обычно двигатель запускается через 7-10&nbsp;секунд, если же этого не произошло в течение 15&nbsp;секунд, то нужно выключить зажигание, подождать 30-60&nbsp;секунд, и снова повторить попытку. При успешном запуске необходимо дать прогреться мотору в течение нескольких минут, в это время оборотами двигателя можно управлять с помощью дроссельной заслонки. Обычно во время прогрева обороты несколько повышены (до&nbsp;2000&nbsp;об/мин), но чрезмерного превышения (выше 2500&nbsp;об/мин) допускать нельзя;</li>
					<li>Если двигатель не завелся, то причина, скорее всего, в залитых бензином свечах (то есть, в переобогащении топливно-воздушной смеси)&nbsp;— об этом свидетельствует выходящий из выхлопной трубы при попытке запуска мотора белый дым. В этом случае нужно открыть дроссельную заслонку (утопить рукоятку) и на&nbsp;10-15&nbsp;секунд включить зажигание. После этого снова повторить процедуру холодного пуска.</li>
				</ol>
				<p>
					 Холодный запуск инжекторных двигателей более прост:
				</p>
				<ol>
					<li>Поставить автомобиль на стояночный тормоз, включить нейтральную передачу, также нужно убедиться, что все электрические приборы в автомобиле выключены;</li>
					<li>Повернуть ключ зажигания в замке в положение II&nbsp;— это включит электронику, а заодно и топливный насос, который произведет закачку топлива. Судить о работе насоса можно по характерному звуку, исходящему со стороны бензобака. Насос должен выключиться сам, после этого нужно вернуть ключ зажигания в исходное положение, а через полминуты снова все повторить;</li>
					<li>Повторить пункты 5 и 6 инструкции по холодному пуску карбюраторного двигателя. За исключением управления дроссельной заслонкой&nbsp;— в инжекторном двигателе управление составом топливной смеси производится электронным блоком;</li>
					<li>Если запуска двигателя не произошло, то нужно подождать 30-60&nbsp;секунд, нажать на педаль газа (примерно до половины), и включить зажигание&nbsp;— это продует цилиндры и высушит залитые бензином свечи. После этого можно повторить процедуру холодного запуска.</li>
				</ol>
				<p>
					 Дизельный двигатель зимой заводится по следующей инструкции:
				</p>
				<ol>
					<li>Поставить автомобиль на стояночный тормоз, установить рычаг переключения скоростей в нейтральное положение, выжать сцепление. Также нужно убедиться, что все электрические приборы отключены;</li>
					<li>Ключ зажигания установить в положение II (или «On»), в автомобилях со свечами накаливания на приборной панели загорится соответствующий индикатор, нужно дождаться, пока он погаснет&nbsp;— это значит, что цилиндры достаточно прогреты и мотор готов к запуску;</li>
					<li>Запустить двигатель, повернув ключ зажигания до упора вправо. При этом для некоторых двигателей необходимо примерно до половины выжать педаль газа (об этом, обычно, сказано в инструкции по эксплуатации автомобиля);</li>
					<li>Пуск двигателя должен произойти в течение 10&nbsp;секунд после поворота ключа, если этого не произошло, то нужно прекратить попытку запуска, а через 20-30&nbsp;секунд снова повторить ее.</li>
				</ol>
