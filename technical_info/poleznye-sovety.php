					 При холодном запуске двигатель подвергается чрезмерным нагрузкам, и главной причиной тому становится увеличение вязкости моторного масла. Густое масло плохо движется по масляным каналам и в первые секунды после запуска двигателя обеспечивает плохую смазку&nbsp;— это приводит к интенсивному износу деталей силового агрегата и сокращению его ресурса. Так что по возможности стоит избегать частых запусков двигателя при сильных морозах.
				</p>
				<p>
					 Но есть и другое решение проблемы холодного запуска — применение специальных средств для облегчения пуска. Например, хорошо помогают автосигнализации с функцией автозапуска, но лучшим решением (особенно для дизельного двигателя) станет предпусковой подогреватель. Потратив немного денег на предпусковой подогреватель, можно не просто облегчить себе жизнь, но и значительно продлить ресурс силового агрегата, так что эта покупка более чем оправдана.
				</p>
				<p>
					 Однако самое главное условие для долгой и надежной работы двигателя&nbsp;— его регулярное техническое обслуживание. Существует несколько общих правил проведения ТО, которые в одинаковой степени подходят для двигателей всех типов.
				</p>
				<p>
					 Техническое обслуживание двигателя делится на несколько видов по периоду проведения:
				</p>
				<p>
					 • Ежедневное ТО (ЕО) — проводится каждый день, причем в начальный и основной периоды эксплуатации двигателя в состав ЕО входят разные мероприятия;<br>
					 • ТО-1000 — проводится на новом автомобиле при пробеге от&nbsp;500 до&nbsp;1000&nbsp;км;<br>
					 • ТО-5500 — проводится на новом автомобиле при пробеге от&nbsp;4500 до&nbsp;5500&nbsp;км;<br>
					 • ТО-1 (первое ТО) — проводится на новом автомобиле при пробеге 10-11&nbsp;тысяч км, в дальнейшем ТО-1 проводится каждые 4-10&nbsp;тысяч км пробега (в зависимости от автомобиля);<br>
					 • ТО-2 (второе ТО) — проводится на новом автомобиле при пробеге 15,5-16,5&nbsp;км, в дальнейшем ТО-2 проводится каждые 12-24&nbsp;тысячи км пробега (в зависимости от автомобиля);<br>
					 • Сезонное техническое обслуживание (СТО) — проводится дважды в год при смене сезона, обычно весной и осенью.
				</p>
				<p>
					 Мероприятия, которые проводятся при каждом виде технического обслуживания, могут отличаться для разных автомобилей, поэтому здесь мы вкратце скажем о наиболее важных из них.
				</p>
				<p>
					 ЕО включает в себя:
				</p>
				<p>
					 • Проверку уровня охлаждающей жидкости, при необходимости производится ее долив. Проверка проводится на холодном двигателе;<br>
					 • Проверка уровня масла в двигателе, при необходимости&nbsp;— долив масла. Проверка проводится через 3-5&nbsp;минут после остановки двигателя;<br>
					 • Визуальный осмотр двигателя, проверка целостности генераторного ремня. Для двигателей с турбокомпрессором&nbsp;— осмотр турбокомпрессора на предмет утечек масла.
				</p>
				<p>
					 ТО-1000 включает следующие мероприятия:
				</p>
				<p>
					 • Очистка силового агрегата от загрязнений; • Проверка топливной системы, системы смазки и системы охлаждения на предмет утечек;<br>
					 • Для двигателей с турбокомпрессором — проверка системы смазки турбокомпрессора;<br>
					 • Проверка целостности и надежности крепления воздушного фильтра, впускного и выпускного коллекторов, турбокомпрессора, топливной аппаратуры и иных деталей;<br>
					 • Проверка степени натяжения и целостности всех приводных ремней или цепей;<br>
					 • Регулировка тепловых зазоров впускных и выпускных клапанов. Проводится на холодном двигателе, порядок проведения зависит от типа и модели двигателя;<br>
					 • Для карбюраторных двигателей проводится регулировка оборотов двигателя на холостом ходу.
				</p>
				<p>
					 ТО-5000 содержит следующие работы:
				</p>
				<p>
					 • Очистка силового агрегата от загрязнений;<br>
					 • Замена моторного масла;<br>
					 • Замена масляного фильтра;<br>
					 • Замена топливного фильтра.
				</p>
				<p>
					 ТО-1 включает следующие мероприятия:
				</p>
				<p>
					 • Мероприятия, проводимые в рамках ТО-1000;<br>
					 • Проверка целостности и работы приводов дроссельной заслонки и т.д.;<br>
					 • Произвести слив отстоя в фильтрах тонкой и грубой очистки топлива, замена топливных фильтров;<br>
					 • Для двигателей с турбокомпрессором — проверить крепление турбокомпрессора, прослушать работу турбокомпрессора на слух.
				</p>
				<p>
					 ТО-2 состоит из следующих мероприятий:
				</p>
				<p>
					 • Замена масляного фильтра;<br>
					 • Замена моторного масла;<br>
					 • Замена топливных фильтров;<br>
					 • Замена или промывка воздушного фильтра;<br>
					 • Проверка работы топливного насоса;<br>
					 • Проверка герметичности топливных магистралей, бака, карбюратора и т.д.;<br>
					 • Регулировка натяжения всех приводных ремней или цепи ГРМ;<br>
					 • Регулировка зазоров клапанов;<br>
					 • Проверка привода акселератора.
				</p>
				<p>
					 Сезонное ТО содержит следующие мероприятия:
				</p>
				<p>
					 • Регулировка угла опережения зажигания (в бензиновых моторах) или впрыска (в дизелях);<br>
					 • В карбюраторных двигателях — перебрать и почистить карбюратор;<br>
					 • Перебрать и почистить топливный насос;<br>
					 • Слить отстой из топливного бака;<br>
					 • Один раз в год рекомендуется промывать топливный бак;<br>
					 • При необходимости — произвести замену или очистку воздушного фильтра.
